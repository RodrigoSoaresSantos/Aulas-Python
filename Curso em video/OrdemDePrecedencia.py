#Operadores aritiméticos

# + = Adição
# - = Subtração
# * = Multiplicação
# / = Divisão
# ** = Potencia
# // = Divisão inteira
# % = Resto da divisão
# **(1/2) = raiz quadra
# **(1/3) = raiz cubica

#Os operadores aritiméticos possuem uma ordem para serem executados. Essa ordem é:

#1 - ()
#2 - **  
#3 - *, /, //, %
#4 - +, -
